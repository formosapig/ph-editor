# colorful
# 0 不能改顏色, 也不讀顏色資料
# 1 只能改主色, 副色強制 (255,255,255,255) (255,255,255,255) 0, 0
# 2 副色只能改光澤質感 (?, ?, ?, ?) (?, ?, ?, ?) Fix, ??
# 3 主副色都能隨意調整
###
# option: [colorful, bottom_enable, bra_enable, panty_enable]
# 
# - colorful:       整數，表示服裝的色彩變化程度,1,2,3 都是多 80 bytes ..
#                   0 = 不能改顏色, 也不讀顏色資料
#                   1 = 只能改主色
#                   2 = 能改副色, 但光澤質感固定
#                   3 = 主副色,光澤顏色,光澤強度及質感都能改.
# - bottom_enable:  0 = 禁止同時穿著 bottom（下身服裝），1 = 允許 2 = 有些可以, 有些不可以...笑死....
# - bra_enable:     0 = 禁止同時穿著 bra（胸罩類），1 = 允許
# - panty_enable:   0 = 禁止同時穿著 panty（內褲類），1 = 允許
# 
# 範例:
# option: [3, 0, 0, 0] 表示色彩度為 3，並禁止穿著 bottom、bra、panty（如圍裙洋裝）
# option: [1, 1, 0, 1] 表示允許穿 bottom/panty，但禁止穿 bra（如連身泳裝）
#
# 部份 top 會導致一些 bottom 不能穿, 主要是半身裙之類的
# 部份 bottom 會導玫一些 top 不能穿, 主要是寬大的裙吧? 整個類不禁, 但個別的會禁...
#
# cut 屬性, 當上半身是連衣裙類, 下半身是會穿模的裙子之類的服裝時, 會禁止選取另一類. 
#     例如上裝是 cut = 1 時, 所有下裝有 cut:1 的都不能選, 反之亦同.
#     當然上裝還有全下裝不能選, 全 bra 不能選. 所以還需要再設計
#
###

