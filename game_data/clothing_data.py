def get_localized_name(name_dict: dict, lang: str = 'ja') -> str:
    '''日文優先（預設為 ja），找不到才退回中文（zh）'''
    return name_dict.get(lang) or name_dict.get('ja') or name_dict.get('zh') or '???'

def get_all_clothing_items(lang: str = 'ja') -> dict:
    """
    獲取所有服裝分類下的所有物品列表。
    Args:
        lang: 語言代碼 ('ja' 或 'zh')。
    Returns:
        一個字典，鍵為服裝分類，值為該分類下的服裝物品列表 (包含 ID 和本地化名稱)。
        ID 仍然是 (原始id, extra) 元組形式。
    """
    return {
        slot: [
            {'id': item['id'], 'name': get_localized_name(item['name'], lang)}
            for item in items
        ]
        for slot, items in CLOTHING_ITEMS.items()
    }

def get_clothing_by_slot(slot: str, lang: str = 'ja') -> list:
    """
    根據分類獲取服裝物品列表。
    Args:
        slot: 服裝分類名稱。
        lang: 語言代碼 ('ja' 或 'zh')。
    Returns:
        該分類下的服裝物品列表 (包含 ID 和本地化名稱)。如果分類不存在則返回空列表。
        ID 仍然是 (原始id, extra) 元組形式。
    """
    items = CLOTHING_ITEMS.get(slot, [])
    return [
        {'id': item['id'], 'name': get_localized_name(item['name'], lang)}
        for item in items
    ]

def get_clothing_by_id(slot: str, item_id: tuple[int, int], lang: str = 'ja') -> str:
    """
    根據分類、原始 ID 和 extra 獲取特定服裝物品的名稱。
    Args:
        slot: 服裝分類名稱。
        item_id: 服裝物品的原始 ID (int, int) 元組。
        lang: 語言代碼 ('ja' 或 'zh')。
    Returns:
        服裝物品的本地化名稱。如果找不到則返回錯誤提示。
    """
    for item in CLOTHING_ITEMS.get(slot, []):
        if item['id'] == item_id:
            return get_localized_name(item['name'], lang)

    not_found_messages = {
        'ja': f'服裝ID {item_id} が見つかりません ({slot})',
        'zh': f'找不到服裝 ID {item_id} ({slot})',
    }
    return not_found_messages.get(lang, f'Clothing ID {item_id} not found ({slot})')

def is_colorful(slot: str, item_id: tuple[int, int]) -> int:
    """
    檢查指定服裝物品是否為可調顏色的。

    Args:
        slot: 服裝分類名稱。
        item_id: 服裝物品的原始 ID (int, int) 元組。

    Returns:
        0: 不能改顏色, 也不讀顏色資料
        1: 只能改主色, 副色固定為白色
        2: 副色只能改光澤質感
        3: 主副色都能隨意調整
    """
    for item in CLOTHING_ITEMS.get(slot, []):
        if item['id'] == item_id:
            option = item.get('option')
            if option and len(option) > 0:
                return option[0]
            else:
                flag = item.get('flag', 0)
                return flag & COLOR_MASK
    return 0  # 如果找不到對應物品，返回 0
    
def is_disabled(slot: str, item_id: tuple[int, int], part: str) -> bool:
    """
    檢查指定服裝物品是否禁用某個部位（如 bottom、bra、panty）。

    Args:
        slot: 服裝分類名稱。
        item_id: 服裝物品的原始 ID (int, int) 元組。
        part: 要檢查的部位，可為 'bottom'、'bra'、'panty'。

    Returns:
        True: 表示該部位被禁用（不能與此服裝同時穿著）
        False: 表示該部位未被禁用（可正常穿著）
    """
    part_index = {'bottom': 1, 'bra': 2, 'panty': 3}
    idx = part_index.get(part)
    if idx is None:
        raise ValueError(f"無效的部位名稱: {part}，必須是 bottom、bra 或 panty")

    for item in CLOTHING_ITEMS.get(slot, []):
        if item['id'] == item_id:
            option = item.get('option', [0, 1, 1, 1])
            # 如果 option 太短，補足預設值 1
            if len(option) <= idx:
                return False  # 預設允許穿著
            return option[idx] == 0
    return False

def is_cut_clothing(slot: str, item_id: tuple[int, int]) -> bool:
    """
    檢查指定服裝是否為 cut 類（限制服裝，可能穿模）。

    Args:
        slot: 服裝分類名稱。僅 top, bottom 兩類...
        item_id: 服裝物品的原始 ID (int, int) 元組。

    Returns:
        True: 表示是 cut 類服裝
        False: 表示不是（或找不到資料）
    """
    if slot not in ('top', 'bottom'):
        raise ValueError(f"無效的 slot: {slot}，僅可為 'top' 或 'bottom'")
    
    CUT_FLAG = Ct # 0b100  # cut 是第 2 位（bit 2）

    for item in CLOTHING_ITEMS.get(slot, []):
        if item['id'] == item_id:
            flag = item.get('flag', 0)
            return (flag & CUT_FLAG) != 0
    return False  # 預設不是 cut

def has_option(slot: str, item_id: tuple[int, int]) -> bool:
    """
    檢查指定泳裝服裝是否具有 option 標記（例如蝴蝶結等額外裝飾）。

    Args:
        slot: 服裝分類名稱。限定為 'swimsuit'。
        item_id: 服裝物品的原始 ID (int, int) 元組。

    Returns:
        True: 表示具有 option 標記。
        False: 表示不具有或找不到資料。
    """
    if slot != 'swimsuit':
        raise ValueError(f"無效的 slot: {slot}，僅可為 'swimsuit'")

    OPTION_FLAG = Op  # 0b1000000

    for item in CLOTHING_ITEMS.get(slot, []):
        if item['id'] == item_id:
            flag = item.get('flag', 0)
            return (flag & OPTION_FLAG) != 0
    return False  # 預設為不具有 option

def get_option_flags(slot: str, item_id: tuple[int, int]) -> dict:
    """
    取得泳裝 option 狀態：是否有上/下 option。
    回傳格式範例：{'option_up': True, 'option_down': False}

    Returns 空字典表示沒有 option。
    """
    if slot != 'swimsuit':
        return {}

    for item in CLOTHING_ITEMS.get(slot, []):
        if item['id'] == item_id:
            flag = item.get('flag', 0)
            result = {}
            if flag & Ou:
                result['option_up'] = None
            if flag & Od:
                result['option_down'] = None
            return result
    return {}
